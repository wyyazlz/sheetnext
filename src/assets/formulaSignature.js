/**
 * Get the signature argument list of a function.
 * @param {string} name - Function name, case-insensitive
 * @returns {string[]|null} Argument display names, or null when unknown
 */
export function getFormulaSignature(name) {
    if (!name) return null;
    return FORMULA_SIGNATURES[String(name).toUpperCase()] ?? null;
}
